    private void displayImage(String imagePath) {
        if (imagePath != null && !imagePath.isEmpty()) {
            ImageIcon ProductImageIcon = new ImageIcon(imagePath);
            Image image = ProductImageIcon.getImage().getScaledInstance(imgProduct.getWidth(), imgProduct.getHeight(), Image.SCALE_SMOOTH);
            imgProduct.setIcon(new ImageIcon(image));
            imgProduct.setToolTipText(imagePath);
        } else {
            imgProduct.setIcon(null);
            imgProduct.setToolTipText("");
        }
    }

    public void populateCategoryComboBox() {
        aModel.removeAllElements();

        listcategory = categoryDAO.selectAll(); // Assuming you have a method to get all categories

        for (Categories category : listcategory) {
            aModel.addElement(category.getName());
        }

        cbxCategoryName.setModel(aModel);
    }

    public void insertOrUpdateInfo() {
        try {
            String productName = txtProductName.getText();
            String description = txtDecription.getText();
            float price = Float.parseFloat(txtPrice.getText());
            int quantity = Integer.parseInt(txtQuantity.getText());
            String categoryName = (String) cbxCategoryName.getSelectedItem();
            String imagePath = imgProduct.getToolTipText();

            Products existingProduct = productDAO.selectById(productName);
            if (existingProduct != null) {
                // Nếu sản phẩm đã tồn tại, cập nhật thông tin sản phẩm
                existingProduct.setDescription(description);
                existingProduct.setPrice(price);
                existingProduct.setQuantityInStock(quantity);
                existingProduct.setCategoriesName(categoryName);
                existingProduct.setImgProduct(imagePath);
                productDAO.update(existingProduct);
                JOptionPane.showMessageDialog(this, "Update success");

                this.dispose();
             

            } else {
                // Nếu sản phẩm chưa tồn tại, chèn thông tin sản phẩm mới
                Products newProduct = new Products(productName, description, price, quantity, categoryName, imagePath);
                productDAO.insert(newProduct);
                JOptionPane.showMessageDialog(this, "Save success");

                this.dispose();
            
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    void setProductInfo(String productName, String category, String description, float price, int quantity, String imagePath) {
        txtProductName.setText(productName);
        txtProductName.setEnabled(false);
        txtDecription.setText(description);
        txtPrice.setText(String.valueOf(price));
        txtQuantity.setText(String.valueOf(quantity));
        cbxCategoryName.setSelectedItem(category);
        displayImage(imagePath);
    }

