        int returnValue = jfile.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            try {
                File file = jfile.getSelectedFile();
                String imagePath = file.getAbsolutePath();
                Xlmage.save(file);
                displayImage(imagePath);

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

